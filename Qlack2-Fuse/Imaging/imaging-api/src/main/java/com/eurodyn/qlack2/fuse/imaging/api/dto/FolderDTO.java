package com.eurodyn.qlack2.fuse.imaging.api.dto;



import java.util.List;

/**
 * Data Transfer object for Folder.
 * @author European Dynamics SA.
 */
public class FolderDTO extends BaseDTO {

    private static final long serialVersionUID = -5258744020788397446L;
    private String name;
    private String description;
    private String parentId;
    private String ownerId;
    private List<ImageAttributeDTO> attributes;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the parentId
     */
    public String getParentId() {
        return parentId;
    }

    /**
     * @param parentId the parentId to set
     */
    public void setParentId(String parentId) {
        this.parentId = parentId;
    }

    /**
     * @return the ownerId
     */
    public String getOwnerId() {
        return ownerId;
    }

    /**
     * @param ownerId the ownerId to set
     */
    public void setOwnerId(String ownerId) {
        this.ownerId = ownerId;
    }

    /**
     * @return the attributes
     */
    public List<ImageAttributeDTO> getAttributes() {
        return attributes;
    }

    /**
     * @param attributes the attributes to set
     */
    public void setAttributes(List<ImageAttributeDTO> attributes) {
        this.attributes = attributes;
    }
}
