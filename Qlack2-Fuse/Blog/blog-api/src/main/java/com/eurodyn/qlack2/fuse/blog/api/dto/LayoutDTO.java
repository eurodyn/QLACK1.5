package com.eurodyn.qlack2.fuse.blog.api.dto;

/**
 * Data Transfer object for  BLOG layout.
 * @author European Dynamics SA.
 */
public class LayoutDTO extends BlogBaseDTO {

    private static final long serialVersionUID = 7750471639790521559L;
    private String name;
    private String home;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the home
     */
    public String getHome() {
        return home;
    }

    /**
     * @param home the home to set
     */
    public void setHome(String home) {
        this.home = home;
    }
}
